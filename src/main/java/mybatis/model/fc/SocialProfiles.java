package mybatis.model.fc;

public class SocialProfiles {

    String type;
    String typdId;
    String typeName;
    String url;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTypdId() {
        return typdId;
    }

    public void setTypdId(String typdId) {
        this.typdId = typdId;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
